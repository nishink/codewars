{-
このプログラムは、与えられた整数 n に対して、x と y の組み合わせを生成し、それらの和を合計するものです。
具体的には、x は 0 から n-1 までの整数を取り、y は 0 から 2n-1 までの整数を取ります。
そして、各組み合わせ (x, y) に対して x + y を計算し、その結果をすべて合計します。

例として、n = 2 の場合を考えてみましょう。
- x は 0 と 1 を取ります。
- y は 0, 1, 2, 3 を取ります。
このとき、組み合わせは以下のようになります：
- (0, 0), (0, 1), (0, 2), (0, 3)
- (1, 0), (1, 1), (1, 2), (1, 3)
これらの組み合わせに対して x + y を計算すると：
- (0, 0) -> 0 + 0 = 0
- (0, 1) -> 0 + 1 = 1
- (0, 2) -> 0 + 2 = 2
- (0, 3) -> 0 + 3 = 3
- (1, 0) -> 1 + 0 = 1
- (1, 1) -> 1 + 1 = 2
- (1, 2) -> 1 + 2 = 3
- (1, 3) -> 1 + 3 = 4
これらの値をすべて合計すると、0 + 1 + 2 + 3 + 1 + 2 + 3 + 4 = 16 となります。
このプログラムは、n の値が大きくなると計算量が増加するため、最適化が必要です。

この問題を最適化するには、x と y の和の計算を直接的に求めることで、ループを避けることができます。
- x は 0 から n-1 までの整数を取ります。したがって、x の合計は (n-1) * n / 2 です。
- y は 0 から 2n-1 までの整数を取ります。
- したがって、y の合計は (2n-1) * 2n / 2 = (2n-1) * n です。
- x の合計を y の個数分だけ加算し、y の合計を x の個数分だけ加算します。
- 最終的な結果は、(xの合計) * (yの個数) + (yの合計) * (xの個数) 
  = ((n-1) * n / 2) * (2n) + ((2n-1) * n) * n
  = (n-1) * n * n + (2n-1) * n^2
  = n^3 - n^2 + 2(n^3) - n^2
  = 3(n^3) - 2(n^2)

したがって、最適化された関数は以下のようになります：

findX :: Int -> Int    
findX n = 3 * (n^3) - 2 * (n^2)

-}

