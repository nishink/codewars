[[0, 0, 1],
 [0, 1, 2],
 [2, 1, 0]]

以下を返す関数を作る。
-1：ボードがまだ完成しておらず、まだ誰も勝っていない場合（空きがあります）、
1：「X」が勝った場合、
2：「O」が勝った場合、
0：それが猫のゲーム（つまり引き分け）である場合。

0が１つでもあったら-1、
