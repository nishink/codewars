-- Financing Plan on Planet XY140Z-n

module Codewars.Kata.Finance where

finance :: Integer -> Integer
finance n = n * (n + 1) * (n + 2) `div` 2

{-
元の地球の例（n = 6）
各週と曜日に応じて貯金額が増えていきます。
週番号を w、曜日を d（0..6）とすると「第0週の各曜日の貯金」「第1週の各曜日の貯金」…と表に並びます。
問題は「週0 から週 n（ここでは 6）まで貯めたとき、
土曜の晩（表で最後まで実行した時点）で合計いくら貯まっているか」を求めるものです。
例として finance(6) = 168 が期待値です。

一般化（惑星 XY140Z-n）
その惑星では曜日が 0 から n までの (n+1) 日/週で、週も 0 から n まで（合計 n+1 週）貯金します。
同じルールで貯めたときの合計を求めます。

結果（閉形式）
この合計は n に対して簡潔な式になります：
finance(n) = n * (n + 1) * (n + 2) / 2

例えば n = 6 のときは 678/2 = 168 となり、期待された値と一致します。

直感的な説明（導出のヒント）
各週ごとの合計や各日の列ごとの合計が等差数列（あるいは三角数の列）になり、
それらを足し合わせると n に関する三次多項式になって閉形式に整理できます。
計算すると上の簡潔な式に帰着します。
-}