-- Tic-Tac-Toe Checker

module TicTacToe where

type Board = [[Int]]

isSolved :: Board -> Int
isSolved board = let
    b = concat board
    c = check b
    in if null c then if elem 0 b then -1 else 0 else head c

line = [[0,1,2],[3,4,5],[6,7,8],[0,3,6],[1,4,7],[2,5,8],[0,4,8],[2,4,6]]
check b = [ x | l <- line, let x = b !! (l !! 0), let y = b !! (l !! 1), let z = b !! (l !! 2), (x == 1 || x == 2) && x == y && y == z ]

board :: Board
board = 
    [[2,1,2],
     [2,1,1],
     [1,1,2]]


